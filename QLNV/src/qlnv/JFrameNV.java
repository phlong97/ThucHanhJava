    public class NhanVien
    {
        String MaNV;
        String PhanXuong;
        int SoSP;
        public NhanVien(){};
        public NhanVien(String ma,String px,int sp)
        {
            MaNV = ma;
            PhanXuong = px;
            SoSP = sp;
        }
        public String getMaNV()
        {
            return this.MaNV;
        }
        public void setMaNV(String ma)
        {
            this.MaNV = ma;
        }
        public String getPhanXuong()
        {
            return this.PhanXuong;
        }
        public void setPhanXuong(String px)
        {
            this.PhanXuong = px;
        }
        public int getSoSP()
        {
            return this.SoSP;
        }
        public void setSoSP(int sp)
        {
            this.SoSP = sp;
        }
        public int getChuan()
        {
            if(PhanXuong.equals("A"))
                return 300;
            else
                return 500;
        }
      
        public boolean VuotChuan()
        {
            return SoSP > getChuan();
        }
        public int TinhLuong()
        {
            if(VuotChuan())
                return (SoSP - getChuan())*30000 + getChuan()*20000;
            else
                return SoSP*20000;
        }
    }

