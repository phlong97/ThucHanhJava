    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblNhanVien.getModel();
        try 
        {                        
            Integer.parseInt(txtSoSP.getText());            
        } 
        catch (NumberFormatException e) 
        {
            lbERR.setText("So san pham chi duoc nhap so");
        }
        if(!txtMa.getText().trim().equals(""))
        {
            NhanVien nv = new NhanVien(txtMa.getText(),cbbPhanXuong.getSelectedItem().toString(),Integer.parseInt(txtSoSP.getText()));
            String vuotchuan = "";
            if (nv.VuotChuan())
                vuotchuan = "x";            
            model.addRow(new Object[]{nv.getMaNV(),nv.getPhanXuong(),txtSoSP.getText(),vuotchuan});
        }
        else
            lbERR.setText("Khong duoc de trong ma san pham");
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        lbERR.setText("");
        DefaultTableModel model = (DefaultTableModel)tblNhanVien.getModel();
        NhanVien nv = new NhanVien(txtMa.getText(),cbbPhanXuong.getSelectedItem().toString(),Integer.parseInt(txtSoSP.getText()));
        String vuotchuan = "";
        if (nv.VuotChuan())
            vuotchuan = "x";
        if(tblNhanVien.getSelectedRow() == -1){
            if(tblNhanVien.getRowCount() == 0){
                lbERR.setText("Khong co du lieu");
            }
            else
                lbERR.setText("Chua chon nhan vien");
        }
        else{
            model.setValueAt(nv.getMaNV(), tblNhanVien.getSelectedRow(), 0);
            model.setValueAt(nv.getPhanXuong(), tblNhanVien.getSelectedRow(), 1);
            model.setValueAt(String.valueOf(nv.getSoSP()), tblNhanVien.getSelectedRow(), 2);
            model.setValueAt(vuotchuan, tblNhanVien.getSelectedRow(), 3);
        }    
        
    }//GEN-LAST:event_btnEditActionPerformed

