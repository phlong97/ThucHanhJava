    private void btnTinhLuongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTinhLuongActionPerformed
        // TODO add your handling code here:
      
        try 
        {                        
            Integer.parseInt(txtSoSP.getText());            
        } 
        catch (NumberFormatException e) 
        {
            JOptionPane.showMessageDialog(null, "Nhap sai, nhap lai");
        }
        int sp = Integer.parseInt(txtSoSP.getText());
        int spc = Integer.parseInt(txtSanPhamChuan.getText());
        int Luong;
        if(sp > spc)    
            Luong = (sp - spc)*30000 + spc*20000;        
        else
            Luong = sp*20000;
        txtLuong.setText(String.valueOf(Luong));
    }//GEN-LAST:event_btnTinhLuongActionPerformed

